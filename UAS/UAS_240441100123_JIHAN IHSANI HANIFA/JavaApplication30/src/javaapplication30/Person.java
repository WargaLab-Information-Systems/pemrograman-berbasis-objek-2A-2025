package javaapplication30;

public abstract class Person {
    private String id;
    private String nama;
    private String alamat;
    
    public Person(String id, String nama, String alamat){
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
    }
    
    public void display_info(){
        
    }
    public class getId {
        return id;
    }
    
    public class getNama {
        return nama;
    }
    
    public class getAlamat {
        return alamat;
    }
    
}
